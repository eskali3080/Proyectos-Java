   public int sumarDivisoresPares(long numero) {
    int s = 0;
    for (int i = 1; i <= numero; i++) {
        if (numero % i == 0 && i % 2 == 0) {
            s += i;
        }
    }
    return s;
}

public int sumarDivisoresImpares(long numero) {
    int s = 0;
    for (int i = 1; i <= numero; i++) {
        if (numero % i == 0 && i % 2 != 0) {
            s += i;
        }
    }
    return s;
}

public int contarDigitos(long numero) {
    int d = 0;
    while (numero != 0) {
        numero /= 10;
        d++;
    }
    return d;
}
